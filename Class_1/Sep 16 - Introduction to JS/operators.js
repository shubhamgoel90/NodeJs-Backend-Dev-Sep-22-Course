// assignment operators
let x = 10; // assigning the value 10 to the variable x
x += 2; // x = x + 2 
x -= 2; // x = x-2;
x *= 2; // x = x*2;
x /= 2; // x = x / 2
x %= 2; // x = x % 2 

// logical operators
console.log((10 > 5) && (7 < 6));
console.log((10 > 5) || (7 < 6));
console.log(4 && 0); // 0
console.log(4 || 0); // 4
console.log(0 && 4); // 0
console.log(0 || 4); // 4
console.log(-4 || 5); //- 4
console.log(3 && 4); // 4
console.log(3 || 4); // 3
